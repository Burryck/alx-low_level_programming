#include <stdio.h>

/**
 * main - a program to print alphabet in lowercae and in uppercase
 *
 * Return: always 0 (Success)
 *
 */

int main(void)
{
	char alp[52] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";

	int i;

	for (i = 0; i < 52; i++)
	{
		putchar(alp[i]);
	}
	putchar('\n');
	return (0);
}
